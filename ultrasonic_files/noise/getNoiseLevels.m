function [dB_2p5V,dB_2p5mV,dB_p25mV] = getNoiseLevels(analysis_method)
% Thomas S. Murray
% 2014/09/25
%
% This function returns the noise levels simulated for different analysis
% methods.
%
% FFT
% +---------+----------+----------+
% |  A (V)  | Min (dB) | Max (dB) |
% +---------+----------+----------+
% |  2.5E-4 | -226.43  | -72.0726 |
% |  2.5E-3 | -206.43  | -52.0726 |
% |  1.0    | -154.39  | - 0.0314 |
% |  2.5    | -146.43  |   7.9274 |
% +---------+----------+----------+
%
% REAL-TIME ADC COUNTS
% +---------+----------+----------+
% |  A (V)  | Min (dB) | Max (dB) |
% +---------+----------+----------+
% |  2.5E-4 | - 151.09 |   4.9564 |
% |  2.5E-3 | - 151.58 |  24.9564 |
% |  1.0    | - 104.34 |  76.9976 |
% |  2.5    | - 96.399 |  84.9564 |
% +---------+----------+----------+
%
% REAL-TIME VOLTS
% +---------+----------+----------+
% |  A (V)  | Min (dB) | Max (dB) |
% +---------+----------+----------+
% |  2.5E-4 | -258.67  | -77.3938 |
% |  2.5E-3 | -238.67  | -57.3938 |
% |  1.0    | -186.63  | - 5.3526 |
% |  2.5    | -178.67  |   2.6062 |
% +---------+----------+----------+
%
% SPECTROGRAM
% +---------+----------+----------+
% |  A (V)  | Min (dB) | Max (dB) |
% +---------+----------+----------+
% |  2.5E-4 | -371.12  | -77.3938 |
% |  2.5E-3 | -362.57  | -57.3938 |
% |  1.0    | -311.38  | - 5.3526 |
% |  2.5    | -303.41  |   2.6062 |
% +---------+----------+----------+

switch(analysis_method)
    case{'fft'}
        dB_2p5V  = ceil(7.9274);
        dB_2p5mV = floor(-52.0726);
        dB_p25mV = floor(-72.0726);
    case{'rtc'}
        dB_2p5V  = ceil(84.9564);
        dB_2p5mV = floor(24.9564);
        dB_p25mV = floor(4.9564);
    case {'rtv'}
        dB_2p5V  = ceil(2.6062);
        dB_2p5mV = floor(-57.3938);
        dB_p25mV = floor(-77.3938);
    case{'spec'}
        dB_2p5V  = ceil(2.6062);
        dB_2p5mV = floor(-57.3938);
        dB_p25mV = floor(-77.3938);
    otherwise
        error('getNoiseLevels: unknown analysis method');
end

return;